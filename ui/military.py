def alert(msg):
    return f"[bold red][ALERT][/bold red] {msg}"

def success(msg):
    return f"[bold green][CONFIRMED][/bold green] {msg}"

def status(msg):
    return f"[bold yellow][STATUS][/bold yellow] {msg}"

def intel(msg):
    return f"[bold cyan][INTEL][/bold cyan] {msg}"

def unit(msg):
    return f"[bold magenta][UNIT][/bold magenta] {msg}"

